'''
Write a Python function to get the largest number, smallest num and sum 
of all from a list.
'''

l = [10,20,30,40,50]

l_num = max(l)

s_num = min(l)

sum_list = sum(l)

print(l_num,"is the largest number in the list\n")

print(s_num,"is the smallest number in the list\n")

print("Sum of all list's values is : ", sum_list)
